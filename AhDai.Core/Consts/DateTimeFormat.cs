namespace AhDai.Core.Consts
{
    /// <summary>
    /// DateTimeFormat
    /// </summary>
    public class DateTimeFormat
    {
        /// <summary>
        /// 标准日期时间格式：yyyy-MM-dd HH:mm:ss
        /// </summary>
        public const string Standard = "yyyy-MM-dd HH:mm:ss";
        /// <summary>
        /// ISO 8601 UTC格式：yyyy-MM-ddTHH:mm:ssZ
        /// </summary>
        public const string Iso8601Utc = "yyyy-MM-ddTHH:mm:ssZ";
        /// <summary>
        /// ISO 8601 带时区偏移格式：yyyy-MM-ddTHH:mm:sszzz
        /// </summary>
        public const string Iso8601WithOffset = "yyyy-MM-ddTHH:mm:sszzz";
    }
}
